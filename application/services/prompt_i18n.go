// ratioDesc 根据画面比例生成描述文本
func ratioDesc(imageRatio string) (orientDesc string, ratioTag string) {
	if imageRatio == "9:16" {
		return "竖屏9:16竖幅画面", "竖屏9:16"
	}
	return "横屏16:9宽幅画面", "横屏16:9"
}

